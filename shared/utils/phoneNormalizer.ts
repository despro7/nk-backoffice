/**
 * Утиліта для нормалізації номерів телефонів до формату +38...
 * 
 * Використовується для стандартизації номерів телефонів клієнтів перед передачею в API
 * (наприклад, для ідентифікації контрагента в Dilovod)
 */

/**
 * Нормалізувати номер телефону до формату +38...
 * 
 * Приклади:
 * - "05012345678" → "3805012345678"
 * - "380501234567" → "380501234567"
 * - "+380501234567" → "380501234567"
 * - "0501234567" (9 цифр) → "3805012345678" (додаємо +38)
 * - "" (пусто) → ""
 * 
 * Логіка:
 * 1. Видаляємо всі не-цифрові символи (пробіли, дефіси, +, дужки тощо)
 * 2. Якщо номер вже починається з 38 — повертаємо як є
 * 3. Якщо номер починається з 0 (український формат) — замінюємо 0 на 38
 * 4. Для інших випадків — додаємо 38 на початок
 * 
 * @param phone Номер телефону у будь-якому форматі
 * @returns Нормалізований номер (без + і нулів на початку) або пусто якщо вхід пустий
 */
export function normalizePhoneNumber(phone: string): string {
  if (!phone) return '';

  // Видаляємо всі символи крім цифр
  const digitsOnly = phone.replace(/\D/g, '');
  if (!digitsOnly) return '';

  // Якщо номер вже починається з 38, повертаємо як є
  if (digitsOnly.startsWith('38')) {
    return digitsOnly;
  }

  // Якщо номер починається з 0 (український формат), додаємо 38
  if (digitsOnly.startsWith('0')) {
    return '38' + digitsOnly.substring(1);
  }

  // Якщо номер не починається ні з 38 ні з 0 (якіось інші цифри),
  // припускаємо що це від закордону без префіксу, додаємо 38
  return '38' + digitsOnly;
}
